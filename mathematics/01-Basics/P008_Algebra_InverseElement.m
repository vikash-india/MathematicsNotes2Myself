# Description: Inverse Element

# Additive Inverse
-1 * 2          # Returns -2. Additive inverse of 2.
-1 * 344        # Returns -344. Additive inverse of 344.

# Multiplicative Inverse
inv(1)          # Returns 1. Multiplicative inverse of 1.
inv(2)          # Returns 0.50000. Multiplicative inverse of 2.
inv(3)          # Returns 0.33333. Multiplicative inverse of 3.

