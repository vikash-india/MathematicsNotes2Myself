# Description: Identity Elements

# Additive Identity
diag(0, 3)          # Returns 3X3 additive identity matrix with all element as 0.
diag(0, 2)          # Returns 2X2 additive identity matrix with all element as 0.
matrix(0, 2, 3)     # Returns 2X3 additive identity matrix with all element as 0.

# Multiplicative Identity
diag(3)             # Returns 3X3 multiplicative identity matrix.
diag(1, 3)          # Same as above. Returns 3X3 multiplicative identity matrix.

# Related Functions
matrix(1, 2, 3)     # Returns 2X3 additive identity matrix with all element as 1.
matrix(2, 3)        # Returns 1X3 Matrix with all element as 2.

