# Description: Rounding Numbers

# Round Up
ceiling(1.2)                # Returns 2
ceiling(1.9)                # Returns 2
ceiling(-1.2)               # Returns -1

# Round Down
floor(1.9)                  # Returns 1
floor(-1.9)                 # Returns -2

# Truncate
trunc(1.23)                 # Returns 1
trunc(1.51)                 # Returns 1
trunc(-1.99)                # Returns -1

# Round
round(1.23)                 # Returns 1
round(1.50)                 # Returns 2
round(1.51)                 # Returns 2

round(-1.23)                # Returns -1
round(-1.50)                # Returns -2
round(-1.51)                # Returns -2

round(341, digits = 2)


