# Description: Inverse Element

library(MASS)

# Additive Inverse
-1 * 2              # Returns -2. Additive inverse of 2.
-1 * 344            # Returns -344. Additive inverse of 344.

# Multiplicative Inverse
ginv(1)             # Returns 1. Multiplicative inverse of 1.
ginv(2)             # Returns 0.50000. Multiplicative inverse of 2.
ginv(3)             # Returns 0.33333. Multiplicative inverse of 3.

