# Description: Identity Elements

# Additive Identity
zeros (3)       # Returns 3X3 additive identity matrix with all element as 0.
zeros(2, 3)     # Returns 2X3 additive identity matrix with all element as 0.

# Multiplicative Identity
eye(3)          # Returns 3X3 multiplicative identity matrix.
eye(2, 3)       # Returns 2X3 multiplicative identity matrix.

# Related Functions
ones(3)         # Returns 3X3 Matrix with all element as 1.
ones(2, 3)      # Returns 2X3 Matrix with all element as 1.

